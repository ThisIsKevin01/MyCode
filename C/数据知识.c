#include <stdio.h>
int main()
{
    char a = -128;
    printf("%u\n", a);
    return 0;
}

// 1) -128转换二进制原码:10000000000000000000000010000000;
// 2) -128转换二进制补码:11111111111111111111111110000000;
// 3) 将数据存放到char a中，进行数据截断: a = 10000000;
// 4) %u打印的是无符号整形数据，对a补1，打印的结果是:11111111111111111111111110000000;
// 5) 结果:4294967168